package posicionandoPivoOrdenandoArray;

public class Notas {
	private String aluno;
	private double valor;
	
	public Notas(String aluno, double valor) {
		
		this.aluno = aluno;
		this.valor = valor;
	}
	public String getAluno() {
		return aluno;
	}
	public double getValor() {
		return valor;
	}
}
